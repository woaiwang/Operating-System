/**
 * =========================================================================
 * delete.c — 文件删除模块（任务B：文件操作层）
 * =========================================================================
 *
 * 本模块实现文件的删除操作。删除流程：清除目录项 → 递减链接计数 →
 * 若计数归零则由 iput() 自动释放数据块和 inode。
 *
 * ┌─────────────────────────────────────────────────────────────────────┐
 * │                    任务A（底层）依赖关系                              │
 * ├─────────────────────────────────────────────────────────────────────┤
 * │ 依赖项            │ 定义位置          │ 说明                          │
 * ├────────────────────┼───────────────────┼──────────────────────────────┤
 * │ namei(name)        │ src/B/name.c:46   │ 任务B：按文件名查inode号      │
 * ├────────────────────┼───────────────────┼──────────────────────────────┤
 * │ iget(dinodeid)     │ src/inode.c:25    │ 任务A：获取内存inode          │
 * │                    │                   │ 先在hinode哈希链查找，        │
 * │                    │                   │ 未命中从磁盘dinode表读取      │
 * ├────────────────────┼───────────────────┼──────────────────────────────┤
 * │ iput(inode)        │ src/inode.c:99    │ 任务A：释放inode引用          │
 * │                    │                   │ i_count==1 && di_number==0时：│
 * │                    │                   │   for(i=0;i<块数;i++)         │
 * │                    │                   │       bfree(di_addr[i])       │
 * │                    │                   │   ifree(i_ino)                │
 * │                    │                   │ 即：释放所有数据块 + 归还     │
 * │                    │                   │ inode到空闲栈                 │
 * ├────────────────────┼───────────────────┼──────────────────────────────┤
 * │ dir 全局变量       │ src/globals.c:23  │ 任务A：当前目录               │
 * │                    │                   │ dir.direct[i]: 目录项         │
 * │                    │                   │ dir.size: 有效条目数          │
 * ├────────────────────┼───────────────────┼──────────────────────────────┤
 * │ DIEMPTY 常量       │ include/filesys.h │ 值=00000，空inode标记         │
 * └────────────────────┴───────────────────┴──────────────────────────────┘
 *
 * 删除语义（类 Unix）：
 *   delete("foo") 只会减少文件的链接计数（di_number）。
 *   当且仅当 di_number 减到 0 时，iput() 才会真正释放数据块和 inode。
 *   这为未来的"硬链接"功能预留了空间 — 多个目录项可指向同一 inode，
 *   删除其中一个目录项不会立即删除文件数据。
 *
 * 注意：本实现仅支持"当前目录内"的文件删除，不支持路径。
 */

#include "filesys.h"   /* 所有结构体、常量、全局变量声明 */
#include <string.h>    /* strcmp(), memset() */
#include <stdio.h>     /* printf() */

/**
 * delete — 从当前目录中删除一个文件
 * @name: 文件名（在当前目录中）
 *
 * 调用链：
 *   delete(name)
 *     → namei(name)           [任务B] 查找文件，获取 inode 号
 *     → memset(dir.direct[i])         清除目录项
 *     → iget(dinodeid)        [任务A] 获取内存 inode
 *     → inode->di_number--            递减链接计数
 *     → iput(inode)           [任务A] 释放引用
 *         └→ 若 i_count==1 && di_number==0:
 *              释放所有数据块(bfree) + 归还 inode(ifree)
 *
 * 注意：delete 不能用于删除目录，这里没有做目录/文件类型区分检查。
 *       在实际使用中，rmdir 应使用不同逻辑（检查目录是否为空）。
 */
void delete(const char *name) {
    unsigned int dinodeid;                   /* namei() 返回的 inode 号 */
    struct inode *inode;                     /* 内存 inode 指针 */
    int i;                                   /* 目录项遍历索引 */

    /* ═══════════════════════════════════════════════════════════════════
     * 步骤1: 在当前目录中查找文件
     * ═══════════════════════════════════════════════════════════════════ */
    dinodeid = namei(name);                  /* [任务B] 遍历 dir.direct[] 按名查找 */
                                             /* 返回 d_ino（磁盘 inode 号） */
    if (dinodeid == 0) {                     /* 未找到（inode号0是无效的） */
        printf("\ndelete: 文件未找到\n");
        return;                              /* 静默返回，不报致命错误 */
    }

    /* ═══════════════════════════════════════════════════════════════════
     * 步骤2: 清除目录项
     * ═══════════════════════════════════════════════════════════════════ */
    /* 遍历目录找到匹配的条目并清零 */
    /* 原始 bug：原代码缺少此步骤，删除后目录项残留， */
    /* 导致 namei 仍能"找到"已删除的文件 */
    for (i = 0; i < dir.size; i++) {         /* 遍历当前目录的所有有效条目 */
        if (dir.direct[i].d_ino == dinodeid &&            /* 条件1: inode号匹配 */
            strcmp(dir.direct[i].d_name, name) == 0) {    /* 条件2: 文件名匹配 */
            /* memset 将整个目录项结构体清零 */
            /* 清零后 d_ino=0 (DIEMPTY)，该槽位可被 iname() 重用 */
            memset(&dir.direct[i], 0, sizeof(struct direct));
            /*       ^^^^^^^^^^^^^^^  ^  ^^^^^^^^^^^^^^^^^^^
             *       目录项地址       0  目录项结构体大小(dirSIZ+4) */
            break;                           /* 找到就退出（同目录不应有重名文件） */
        }
    }

    /* ═══════════════════════════════════════════════════════════════════
     * 步骤3: 获取 inode 并递减链接计数
     * ═══════════════════════════════════════════════════════════════════ */
    inode = iget(dinodeid);                  /* [任务A] 获取内存 inode（引用计数+1） */
    if (!inode) return;                      /* iget 失败（理论上不会，因为 namei 刚找到） */

    /* 递减硬链接计数 */
    /* di_number 表示有多少个目录项指向此 inode */
    /* 普通文件创建时 di_number=1，每多一个硬链接 +1 */
    if (inode->di_number > 0)                /* 防御性检查：防止下溢 */
        inode->di_number--;                  /* 链接计数 -1 */
    /* 原始 bug：原代码无条件 di_number--，可能导致无符号下溢成 65535 */

    /* ═══════════════════════════════════════════════════════════════════
     * 步骤4: 释放 inode 引用
     * ═══════════════════════════════════════════════════════════════════ */
    iput(inode);                             /* [任务A] 释放 inode 引用 */
                                             /* iput 内部逻辑： */
                                             /*   - 若 i_count > 1：仅递减引用计数 */
                                             /*   - 若 i_count == 1（最后一个引用）： */
                                             /*       - di_number != 0：写回 dinode 到磁盘 */
                                             /*       - di_number == 0：释放所有数据块(bfree) */
                                             /*                         + 归还 inode(ifree) */
                                             /* 因此，当 di_number 减到 0 时，文件数据被真正释放 */
}
